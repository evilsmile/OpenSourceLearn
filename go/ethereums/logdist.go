type Hash [32]byte

// logdist returns the logarithmic distance between a and b, log2(a ^ b)
func logdist(a, b Hash) int {
	lz := 0
	for i := range a {
		x := a[i] ^ b[i]
		fmt.Println("x: ", x)
		if x == 0 {
			lz += 8
		} else {
			lz += lzcount[x]
			break
		}
	}
	return len(a)*8 - lz
}

func main() {
	a := [32]byte{0x1, 0x51, 0x33}
	b := [32]byte{0x1, 0x2, 0x22}
	fmt.Printf("a:%x\n", a)
	fmt.Printf("b:%x\n", b)
	var c Hash
	for i := range a {
		c[i] = a[i] ^ b[i]
		if c[i] != 0 {
			break
		}
	}
	fmt.Printf("c:%x\n", c)
	fmt.Println()
	fmt.Println(logdist(a, b))
}
